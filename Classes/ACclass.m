            [~,MW2B] = obj.Wind2Body([0;0;0],'W2B',alphar,betar);      % Wind to Body axes. angles in radiants
            [~,MB2N] = obj.body2NED([1;0;0],'B2N',x(12),x(11),x(10)); % Body 2 NED matrix psi teta phi
            %% Forces
            % Aerodynamic
            L = q0*obj.Sw*( obj.CL(M,Re,alpha) + obj.CLde(M,Re,u(2)) + ...
