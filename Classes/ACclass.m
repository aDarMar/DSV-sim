            obj.CMq(M,Re)*x(5,i)*obj.cref*0.5/V0 , obj.CMde(M,Re,u(2)),...
            ( obj.CM(M,Re,alpha) + ...
            obj.CMq(M,Re)*x(5,i)*obj.bw*0.5/V0 + obj.CMde(M,Re,u(2)) ) ];
        temp3(i,:) = [Roll,Pitch,Yaw];
